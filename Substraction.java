class Substraction
{
    public static void main(String a[])
	{
	   System.out.println(sub(129,89));
	   System.out.println(sub(897,578));
	   System.out.println(sub(986,587,112));
	   
	}
   
      public static int sub(int a, int b)
	  {
	  
	     return a-b;
	  
	  }
	  
	  public static int sub(int a, int b, int c)
	  {
		  return a-b-c;
	  }

}
